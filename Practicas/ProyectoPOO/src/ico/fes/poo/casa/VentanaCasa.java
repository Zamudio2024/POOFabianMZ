    private void campos() {

        if (jTextField1.getText().isEmpty()) {
            jLabel5.setText("*Campo obligatorio.");
        } else {
            jLabel5.setText("");
        }

        if (jTextField2.getText().isEmpty()) {
            jLabel6.setText("*Campo obligatorio.");
        } else {
            jLabel6.setText("");
        }

        if (jTextField3.getText().isEmpty()) {
            jLabel7.setText("*Campo obligatorio.");
        } else {
            jLabel7.setText("");
        }

        if (jTextField4.getText().isEmpty()) {
            jLabel8.setText("*Campo obligatorio.");
        } else {
            jLabel8.setText("");
        }
    }

    private void actualizar() {
        jTextField1.setText("");
        jTextField2.setText("");
        jTextField3.setText("");
        jTextField4.setText("");
        jButton2.setEnabled(false);
        campos();
    }

    private void validar() {
        campos();
        if (jTextField1.getText().isEmpty() || jTextField2.getText().isEmpty() || jTextField3.getText().isEmpty() || jTextField4.getText().isEmpty()) {
            jButton2.setEnabled(false);

        } else {
            jButton2.setEnabled(true);
        }

    }

    private void validarCampos() {
        if (jTextField1.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Favor de agregar el material de la casa.");
        }
        if (jTextField2.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Favor de agregar el numero de cuartos.");
        }
        if (jTextField3.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Favor de agregar el numero de patios.");
        }
        if (jTextField4.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Favor de agregar el numero de plantas.");
        }
    }


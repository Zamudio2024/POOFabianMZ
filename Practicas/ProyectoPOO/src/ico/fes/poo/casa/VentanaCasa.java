    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        ArrayList<Casa> c = new ArrayList();
        campos();
        c.add(new Casa("Concreto", 5, 2, 15));
        c.add(new Casa("Madera", 4, 1, 20));
        modelo = new AbstractModeloTabla(c);
        jTable1.setModel(modelo);
        jTable2.setModel(modelo);
    }//GEN-LAST:event_jButton1MouseClicked

    private void jButton2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton2MouseClicked
        
        if (jTextField1.getText().isEmpty() || jTextField2.getText().isEmpty() || jTextField3.getText().isEmpty() || jTextField4.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Favor de colocar todos los campos");
            validarCampos();
        } else {
            Casa s = new Casa();
            s.setMaterial(jTextField1.getText());
            s.setNumCuartos(Integer.parseInt(jTextField2.getText()));
            s.setNumPatios(Integer.parseInt(jTextField3.getText()));
            s.setNumPlantas(Integer.parseInt(jTextField4.getText()));
            modelo.addCasa(s);
            actualizar();
        }

    }//GEN-LAST:event_jButton2MouseClicked

